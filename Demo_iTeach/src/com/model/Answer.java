package com.model;

public class Answer {
	String student_id;
	String paper_id;
	int flag;
	String selection_as;
	String judgement_as;
	String simple_as;
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getPaper_id() {
		return paper_id;
	}
	public void setPaper_id(String paper_id) {
		this.paper_id = paper_id;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	public String getSelection_as() {
		return selection_as;
	}
	public void setSelection_as(String selection_as) {
		this.selection_as = selection_as;
	}
	public String getJudgement_as() {
		return judgement_as;
	}
	public void setJudgement_as(String judgement_as) {
		this.judgement_as = judgement_as;
	}
	public String getSimple_as() {
		return simple_as;
	}
	public void setSimple_as(String simple_as) {
		this.simple_as = simple_as;
	}
	

}
